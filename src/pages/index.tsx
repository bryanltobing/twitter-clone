import { SignInButton, useUser } from "@clerk/nextjs";
import { type NextPage } from "next";
import Head from "next/head";
import Image from "next/image";

import { type RouterOutputs, api } from "~/utils/api";

import dayjs from "dayjs";
import relativeTime from "dayjs/plugin/relativeTime";
import { LoadingSection } from "~/components/Loading";

dayjs.extend(relativeTime);

const CreateTweetWizard = () => {
  const { user } = useUser();

  if (!user) return null;

  return (
    <div className="flex gap-3">
      <Image
        src={user.profileImageUrl}
        alt="profile image"
        width={48}
        height={48}
        className="rounded-full"
      />
      <input
        placeholder="What's happening?"
        className="grow bg-transparent outline-none"
      />
    </div>
  );
};

type TweetWithAuthor = RouterOutputs["tweets"]["getAll"][number];

const TweetView = (props: TweetWithAuthor) => {
  const { author, tweet } = props;

  return (
    <div className="flex items-center gap-4 border-b border-slate-400 p-4">
      <Image
        src={author.profileImageUrl}
        alt={`${author.username}'s profile picture`}
        width={48}
        height={48}
        className="rounded-full"
      />

      <div className="flex flex-col">
        <div className="flex gap-1 text-slate-300">
          <span>{`@${author.username}`}</span>Â·
          <span className="font-thin">{dayjs(tweet.createdAt).fromNow()}</span>
        </div>
        <span className="text-xl">{tweet.content}</span>
      </div>
    </div>
  );
};

const Feed = () => {
  const {
    data: tweets = [],
    isLoading,
    isError,
  } = api.tweets.getAll.useQuery();

  if (isLoading) {
    return <LoadingSection />;
  }

  if (isError) {
    return <div>Something went wrong</div>;
  }

  if (!tweets.length) {
    return <div>There is no tweets just yet</div>;
  }

  return (
    <div className="flex flex-col">
      {tweets.map((tweetWithAuthor) => (
        <TweetView {...tweetWithAuthor} key={tweetWithAuthor.tweet.id} />
      ))}
    </div>
  );
};

const Home: NextPage = () => {
  const { isLoaded, isSignedIn } = useUser();

  api.tweets.getAll.useQuery();

  return (
    <>
      <Head>
        <title>Twitter Clone</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex h-screen justify-center">
        <div className="h-full w-full border-x border-slate-400 md:max-w-2xl">
          {isLoaded && (
            <div className="border-b border-slate-400 p-4">
              {!isSignedIn ? <SignInButton /> : <CreateTweetWizard />}
            </div>
          )}

          <Feed />
        </div>
      </main>
    </>
  );
};

export default Home;
